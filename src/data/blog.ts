export function getBlogPosts(locale: string): BlogPost[] {
  return blogPosts[locale] || [];
}

export function getBlogPost(slug: string, locale: string): BlogPost | null {
  const posts = getBlogPosts(locale);
  return posts.find(post => post.slug === slug) || null;
}

export function getFeaturedPosts(locale: string): BlogPost[] {
  const posts = getBlogPosts(locale);
  return posts.filter(post => post.featured);
}

// New function to find corresponding post in target language
export function findCorrespondingPost(currentSlug: string, currentLocale: string, targetLocale: string): string | null {
  // Find the current post
  const currentPost = getBlogPost(currentSlug, currentLocale);
  if (!currentPost) return null;
  
  // Find the corresponding post in target language by ID
  const targetPosts = getBlogPosts(targetLocale);
  const correspondingPost = targetPosts.find(post => post.id === currentPost.id);
  
  return correspondingPost ? correspondingPost.slug : null;
} 